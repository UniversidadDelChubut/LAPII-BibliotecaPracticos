package ejerciciosexcepciones.poligonos;

/**
 * Representa un punto en el plano coordenado.
 * 
 * @author sadellatorre
 */
public class Punto {
	
	private double x;
	private double y;
	
	public Punto(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}
	

	public double getX() {
		return x;
	}
	
	public double getY() {
		return y;
	}
	
}
